package fr.alecharp.jimmy.back.http;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.http.MediaType;
import org.springframework.security.core.annotation.AuthenticationPrincipal;
import org.springframework.security.core.userdetails.User;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class PingAPI {
    private static final Logger LOG = LoggerFactory.getLogger(PingAPI.class);

    @GetMapping(value = "/api/ping", produces = MediaType.APPLICATION_JSON_UTF8_VALUE)
    public String ping(@AuthenticationPrincipal User user) {
        LOG.debug("{}", user);
        return "pong";
    }
}
